<span class="title label label-default">BerkaPhp</span>

<div class="box">
    berkaPhp is a PHP MVC framework that helps you quickly write a web application and gives full controller over your application.
</div>

<h3 class="sub_title">Why you may give berkaPhp a try</h3>
<div class="box">
    You don't always need to write web application solutions using framework like cakePhp, Symfony or Laravel. These are great framework, without a doubt. But they are often overkill. especially when you just need to develop a simple web application or web site with a backend , berkaPhp only give a minimal set of tools that do what you need.
</div>

<h3 class="sub_title">Rapid development</h3>
<div class="well">
    <span>You can Quickly build a web application / web site</span>
</div>

<h3 class="sub_title">No complicated Configuration</h3>
<div class="well">
    <span>No complicated XML files. Just setup your database and you're ready to generate files and use them.</span>
</div>

<h3 class="sub_title">Simple framework structured</h3>
<div class="well">
    <span>Clean and simple MVC Conventions and easy to understand and use.</span>
</div>

